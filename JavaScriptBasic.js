/*
    JavaScript is an interpreted language, so it is not compiled it is interpreted. As a result,
    interpreter will read code line by line and execute that code line by line.

    Each browser has its own JavaScript engine, which either interprets the code, or uses some sort of
    lazy compilation.

    Browsers JavaScript engine names:
        V8: Chrome and Node.js
        SpiderMonkey: Firefox
        JavaScriptCore: Safari
        Chakra: Microsoft Edge/IE
    
    Node.js also uses V8 engine.

    They each implement the ECMAScript standard, but may differ for anything not defined by the standard.
*/