// var will allow to assign a new value to a variable
var varName = 'Harsh'
console.log(varName)

varName = 'Sharma'
console.log(varName)

// let will allow to assign a new value to a variable
let letName = 'Harsh'
console.log(letName)

letName = 'Sharma'
console.log(letName)

// const will not allow to assign a new value to a variable
const constName = 'Harsh'
console.log(constName)

constName = 'Sharma'
console.log(constName) // Will throw an error -> Assignment to constant variable