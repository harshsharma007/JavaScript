/*
    ES5, ES6, ES2016, ES2017, ES.Next
    Transpilers (Babel, TypeScript, CoffeeScript etc)

    ECMAScript vs JavaScript
    ECMAScript defines specification and tells exactly what this language should do and how much of these
    functions behave.
    JavaScript is actually an implementation of ECMAScript specification.

    What is transpiler?
    Or
    How the backward compatibility is managed in ES series?
    A transpiler is bascially some code that goes from newer language features and makes it backward 
    compatible with the ES5 spec.
    Transpiler picks the new code of new language (TypeScript, Babel) and converts it back to ES5.
*/